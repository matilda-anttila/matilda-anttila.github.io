function setLanguage(lang) {
  const current = translations[lang] || translations.en;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (current[key] !== undefined) {
      element.innerHTML = current[key];
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    const attrMap = element.getAttribute("data-i18n-attr").split(";");
    attrMap.forEach((pair) => {
      const [attr, key] = pair.split(":").map((item) => item.trim());
      if (attr && key && current[key] !== undefined) {
        element.setAttribute(attr, current[key]);
      }
    });
  });

  document.documentElement.lang = lang;
  document.title = current.page_title || translations.en.page_title;

  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription && current.meta_description) {
    metaDescription.setAttribute("content", current.meta_description);
  }

  localStorage.setItem("siteLanguage", lang);

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });
}

document.querySelectorAll(".lang-btn").forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.dataset.lang);
  });
});

const savedLanguage = localStorage.getItem("siteLanguage") || "en";
setLanguage(savedLanguage);