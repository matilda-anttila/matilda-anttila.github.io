function setLanguage(lang) {
  const current = translations[lang] || translations.en;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (current[key] !== undefined) {
      element.innerHTML = current[key];
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    const attrMap = element.getAttribute("data-i18n-attr").split(";");
    attrMap.forEach((pair) => {
      const [attr, key] = pair.split(":").map((item) => item.trim());
      if (attr && key && current[key] !== undefined) {
        element.setAttribute(attr, current[key]);
      }
    });
  });

  document.documentElement.lang = lang;
  document.title = current.page_title || translations.en.page_title;

  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription && current.meta_description) {
    metaDescription.setAttribute("content", current.meta_description);
  }

  localStorage.setItem("siteLanguage", lang);

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });

  updateExperienceToggleText();
}

function updateExperienceToggleText() {
  const toggle = document.getElementById("experienceToggle");
  if (!toggle) return;

  const lang = document.documentElement.lang in translations ? document.documentElement.lang : "en";
  const current = translations[lang];
  const expanded = toggle.getAttribute("aria-expanded") === "true";

  toggle.textContent = expanded ? current.show_less : current.show_more;
}

function setupExperienceToggle() {
  const toggle = document.getElementById("experienceToggle");
  const hiddenItems = document.querySelectorAll(".experience-extra");

  if (!toggle || !hiddenItems.length) return;

  let expanded = false;

  hiddenItems.forEach((item) => {
    item.hidden = true;
  });

  toggle.setAttribute("aria-expanded", "false");

  toggle.addEventListener("click", () => {
    expanded = !expanded;

    hiddenItems.forEach((item) => {
      item.hidden = !expanded;
    });

    toggle.setAttribute("aria-expanded", expanded ? "true" : "false");
    updateExperienceToggleText();
  });

  updateExperienceToggleText();
}

document.querySelectorAll(".lang-btn").forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.dataset.lang);
  });
});

setupExperienceToggle();

const savedLanguage = localStorage.getItem("siteLanguage") || "en";
setLanguage(savedLanguage);